#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - a function returns the sum of two numbers_______
 * @a: the frist number____
 * @b: the second number ____
 * Return: returns always (a + b) ______
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 *op_sub - a function returns the difference of two numbers_______
 * @a: the frist number____
 * @b: the second number ____
 * Return: returns always (a - b) ______
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul- a function returns the product of two numbers_______
 * @a: the frist number____
 * @b: the second number ____
 * Return: returns always (a * b) ______
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - a function returns the result of the division of two numbers__
 * @a: the frist number____
 * @b: the second number ____
 * Return: returns always (a / b) ______
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - a function returns the result of the remainder of the division
 * @a: the frist number____
 * @b: the second number ____
 * Return: returns always (a % b) ______
 */

int op_mod(int a, int b)
{
return (a % b);
}
